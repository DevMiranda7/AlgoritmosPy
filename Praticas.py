# Cronômetro para Seu Recorde

# 1. Abra o cronômetro no PC ou celular.

# 2. Escolha o algoritmo (ex: Fatorial, Fibonacci, Ordenação).

# 3. Inicie o cronômetro assim que começar.

# 4. Anote o tempo de execução quando terminar.

# 5. Desafie-se a melhorar seu tempo em próximos testes.

# Exemplo de Registro:
# Algoritmo: Fatorial Recursivo
# Entrada: fatorial(20)
# Tempo: 0.0032 segundos

# Teste e melhore seus tempos sempre que possível!


